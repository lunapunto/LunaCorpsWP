global.is_waypoint = function(e){
  var hwindow = $(window).height();
  var ot = $(window).scrollTop();
  var ob = ot + hwindow;
  var et = e.offset().top + e.outerHeight();
  if(ot <= et && et <= ob){
    return true;
  }else{
    return false;
  }
}
